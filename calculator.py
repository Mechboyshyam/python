# Calculator using python language
number1 = int(input("Enter the value of a :"))
number2 = int(input("Enter the value of b :"))

addition = number1 + number2
print("The addition of ",number1, "+", number2, "is",addition)

substraction = number1 - number2
print("The addition of ",number1, "-", number2, "is",substraction)

multiplicaton = number1 * number2
print("The addition of ",number1, "*", number2, "is",multiplicaton)

tothepower = number1 ** number2
print("The addition of ",number1, "**", number2, "is",tothepower)

division = number1 / number2
print("The addition of ",number1, "/", number2, "is",division)

floor = number1 // number2
print("The addition of ",number1, "//", number2, "is",floor)